<h2>Advice on How to Start an Anime Club</h2>

<p>
People often write us to ask for advice on how to start an anime
club. Unfortunately, our club is now so old that no one really remembers
how we started it.  Those who might remember, sadly enough, have scrambled
their brain-cells by watching too much ancient anime and are now only
able to communicate in a fractured pidgin <i>Nihongo</i>.  Consider yourself warned. 

<h3>Finding clubs in your area</h3>

<p> If you haven't done so already, take a look at the <a
href="http://www.anipike.com">Anime Web Turnpike</a>.  In particular,
their geographic guide to <a href=http://anipike.com/index.php?env=-inlink/index:m111-1-1-3-s&reset=1">

anime clubs in the United States</a> and <a
href="http://anipike.com/index.php?env=-inlink/index:m112-1-1-3-s">anime clubs outside the
US</a> may help you find a club near you.  Local clubs may have good local
advice, and may agree to swap DVDs with you, giving you access
to yet more anime.</p>
<h3>Convincing parents, teachers, etc., that an anime club is worthwhile</h3>

<p> If you're high school students looking to start a club, you may
find this <a href="http://www.abcb.com/parents/"><i>Parents' guide to
anime</i></a> helpful when arguing that there are anime which can be
shown on campus without inundating the school administration with
calls from irate parents ranting about the school showing violent
animated Japanese pornography.  The books listed
at our <a href="http://library.mit.edu/F/SY9LQT4G1URB1X9P1CA3XVKXFIBEKHNLUYP52D7T4FHGJ1AEMR-31936?func=find-a&find_code=WRD&request=anime&request_op=OR&find_code=WRD&request=manga&request_op=AND&find_code=WRD&request=&adjacent=N&filter_code_2=WYR&filter_request_2=&local_base=MIT01PUB&filter_code_4=WLB&filter_request_4=&filter_code_3=WYR&filter_request_3=&filter_code_1=WLG&filter_request_1=&filter_code_5=WCL&filter_request_5=&Search=Search">anime-related books link</a> may help convince your
school's faculty that an interest in anime might serve an educational
purpose.  In particular, see <a
href="http://www.koyagi.com/teachers.html">Gilles Poitras' Teacher's
companion to <i>The anime companion</i></a>.



<h3>Club activities</h3>
<p>In addition to showing anime, our club has had fun with the following
activities:</p>

<ul class="p">
	<li>Group discounts at local cons (with the more dedicated
	    going to non-local cons as well).</li>
	
	<li>A cosplay contest, particularly around Halloween.</li>

	<li>Cosplay workshops: get together and share ideas,
	    techniques, and sewing machines.</li>

	<li>Karaoke events using anime or J-Pop music with timed lyrics.</li>

	<li>Group expeditions to local Japanese restaurants and/or
	    bookstores.</li>

	<li>Inviting speakers on anime-related topics: animation,
	    comics, film, popular culture, or Japanese culture.</li>

	<li>A club newsletter.</li>

</ul>

<h3>Financing acquisitions</h3>

<p>Building an extensive anime library requires a fair amount of
money, it also requires a good deal of patience --- we have been 
building our library since 1991.  </p>

<p>
We have financed our club operations in the following ways: </p>
<ul class="p">
	<li>University (and perhaps high-school) clubs may be able to
	    get funding as student activities.  </li>

	<li>University (and perhaps high-school) clubs may fall under
	    the "educational, non-profit" umbrella of their parent
	    institution, letting you reward contributions with a
	    letter that will permit the contributor to take the value
	    of their contribution as a tax-deduction.</li>

	<li>We charge a small membership fee.  This fee goes to
	    defraying the costs of running the club (buying anime and other media for
	    the library, advertising, hosting various showings and events). </li>

	<li>Since our showings are at dinner time, one of us takes
	    pizza orders and gets pizza and soda which we sell at a
	    small profit.  We get about a quarter of our funding this
	    way.</li>

	<li>We have a (clean) jar that we put on the front desk for
	    people to drop small change into.    After a semester, that change really add up.
      It's silly, but it works. </li> 
</ul>

<h3>Getting permission to show anime</h3>

<p>If your club grows larger than a handful of friends, your showings
become public exhibitions, and you should get permission to show the
anime you are screening; this way, you don't have to pretend you're
distracted when the "FBI Warning" comes onto the screen.
Most companies recognize the promotional opportunity that club
showings represent, and are happy to give permission for you to show
the material they have licensed.</p>

<p>These days, getting permission has gotten pretty easy since most
companies have email addresses for fan relations, and these people can
usually at least put you in contact with the person who can authorize
a showing.  Some companies even have web-forms you can fill out to get
showing permissions!</p>

<p><a href="http://www.koyagi.com/showpermissions.html">
Gilles Poitras maintains a page that will
help you find out how to get screening permissions.</a>

<h3>High school clubs</h3>

<p>Is the club just a gathering place for people who like anime to come
to meetings and talk? If this is the case, you might not have a big
membership depending on your classmates.  For an anime club to be
successful, it generally needs actual anime, owned either by the club
or officers/members, that can be accessed by other members in some
way. Either that, or you're going to need a bunch of hard-core fans
who like just talking about anime.</p>

<p> High school clubs are a little tricky, since they generally have more
rules to work.
Some clubs have short showings once a week or a few times a month during lunch or after school, in somewhere like a club supervisor's room.  The
anime can come from the officers' collections, though one has to be
careful what is shown since many high schools are conservative and may
refuse certain shows for violence or sexual themes.  One way around that
is to have a few parties during some holidays at various
officers/members' houses.</p>

<p>If you can hold meetings outside of school time, that's great, since
showings can be longer. If your club is allowed to own anime and keep
a library, even better. </p>


<p>It's usually good to have a low (or even no) membership fee, since
that brings in more people. Club dues are only needed if you're going
to do stuff that takes more than pocket money to accomplish as a club.</p>

<h3>Helpful resources</h3>
<p>
A good place to start is Gilles Poitras's book, <i>Anime
essentials: every thing a fan needs to know</i> (Stone Bridge Press,
2001).  <i>Anime essentials</i> is a handbook of US and Canadian otaku
culture, and contains a chapter with pointers on how to start your own
anime club.</p>

